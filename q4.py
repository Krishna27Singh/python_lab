a = int(input("Enter the value of first number"))
b = int(input("Enter the value of second number"))

c = input("Which operation do you wanna perform? ")

if(c == "+"):
    print(a+b)
if(c == "-"):
    print(a-b)
if(c == "*"):
    print(a*b)
if(c == "/"):
    print(a/b)
if(c == "%"):
    print(a%b)
if(c == "**"):
    print(a**b)
