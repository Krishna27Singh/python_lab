import math as math

fact = int(input("Enter the number whose factorial you want to calculate: "))
print(f"The factorial of {fact} is {math.factorial(fact)}")

sqrt = int(input("Enter the number whose square root you want to calculate: "))
print(f"The square of the given number is: {math.sqrt(sqrt)}")

gcd = int(input("Enter the number whose gcd you want to calculate"))
print(f"The gcd of the given number is: {math.gcd(gcd)}")

